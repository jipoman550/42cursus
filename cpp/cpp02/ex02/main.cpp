#include "Fixed.hpp"

// 서브젝트(15페이지)에서 제공된 테스트 코드
// 기대 출력:
// 0
// 0.00390625    ← 전위 ++로 1/256이 더해진 결과
// 0.00390625
// 0.0078125     ← 후위 ++로 한 번 더 더해진 결과
// 10.1016       ← a * b의 결과
// 10.1016       ← Fixed::max(a, b)의 결과
int main(void)
{
	Fixed		a;                          // 기본 생성자: 값 = 0
	Fixed const	b(Fixed(5.05f) * Fixed(2)); // 5.05 * 2 = 10.1 을 고정 소수점으로 연산

	// [1] a의 초기값 출력: 0
	std::cout << a << std::endl;

	// [2] 전위 증가(++a): val이 1 증가 → 1/256 = 0.00390625가 된 후의 값을 출력
	std::cout << ++a << std::endl;

	// [3] 현재 a의 값을 출력: 여전히 0.00390625
	std::cout << a << std::endl;

	// [4] 후위 증가(a++): 현재 값(0.00390625)을 먼저 출력하고, 그 후 val이 1 증가
	// → 출력되는 것은 증가 전 값(0.00390625)이지만
	// 실제 a는 이제 val이 2가 되어 0.0078125
	std::cout << a++ << std::endl;

	// [5] 후위 증가 이후 a의 현재 값 출력: 0.0078125
	std::cout << a << std::endl;

	// [6] b의 값 출력: 5.05 * 2 = 10.1 → 고정 소수점 근사값 10.1016
	std::cout << b << std::endl;

	// [7] Fixed::max(a, b): a(0.0078125)와 b(10.1016) 중 큰 값인 b를 출력
	std::cout << Fixed::max(a, b) << std::endl;

	return (0);
}
