#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	if (!s || fd < 0)
		return (0);
	while (*s)
	{
		if (write(fd, s, 1) == -1)
			return (-1);
		s++;
	}
	return (0);
}
